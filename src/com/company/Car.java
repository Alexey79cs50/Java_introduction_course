package com.company;

//class definition
public class Car {
    String orderID, brand, model, colour, regNum;
    int year;

    //constructor
    Car(String orderID, String brand, String model, String colour, String regNum, int year ){
        this.orderID = orderID;
        this.brand = brand;
        this.model = model;
        this.colour = colour;
        this.regNum = regNum;
        this.year = year;
    }
}

